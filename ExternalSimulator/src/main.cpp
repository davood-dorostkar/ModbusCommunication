void SendRelayData()
{
  int rowCount = sizeof(RelayRequests) / sizeof(RelayRequests[0]);
  for (int i = 0; i < rowCount; i++)
  {
    for (int j = 0; j < 6; j++)
    {
      Serial.write(RelayRequests[i][j]);
    }
    delay(100);
  }
}
void SendMainboardData()
{
  int rowCount = sizeof(MainboardRequests) / sizeof(MainboardRequests[0]);
  for (int i = 0; i < rowCount; i++)
  {
    for (int j = 0; j < 6; j++)
    {
      Serial.write(MainboardRequests[i][j]);
    }
    delay(100);
  }
}

void setup()
{
  Serial.begin(115200);
}

void loop()
{
  SendRelayData();
  delay(500);
  SendMainboardData();
  delay(5000);
}
